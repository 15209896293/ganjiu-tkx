def add_heading_styled(text, level=1):
    h = doc.add_heading(text, level=level)
    for run in h.runs:
        run.font.name = '微软雅黑'
        run._element.rPr.rFonts.set(qn('w:eastAsia'), '微软雅黑')
    return h

def add_para(text, bold=False, size=11, color=None, alignment=None, spacing_after=6, first_line_indent=None):
    p = doc.add_paragraph()
    if alignment is not None:
        p.alignment = alignment
    pf = p.paragraph_format
    pf.space_after = Pt(spacing_after)
    pf.space_before = Pt(2)
    if first_line_indent:
        pf.first_line_indent = Cm(first_line_indent)
    run = p.add_run(text)
    run.font.name = '微软雅黑'
    run._element.rPr.rFonts.set(qn('w:eastAsia'), '微软雅黑')
    run.font.size = Pt(size)
    run.bold = bold
    if color:
        run.font.color.rgb = RGBColor(*color)
    return p

def add_bullet(text, level=0):
    p = doc.add_paragraph(style='List Bullet')
    p.clear()
    run = p.add_run(text)
    run.font.name = '微软雅黑'
    run._element.rPr.rFonts.set(qn('w:eastAsia'), '微软雅黑')
    run.font.size = Pt(11)
    return p

def set_cell_text(cell, text, bold=False, size=10, alignment=None, bg_color=None):
    for paragraph in cell.paragraphs:
        for run in paragraph.runs:
            run.text = ''
    p = cell.paragraphs[0]
    if alignment is not None:
        p.alignment = alignment
    if bg_color:
        shading_elm = parse_xml(f'<w:shd {nsdecls("w")} w:fill="{bg_color}"/>')
        cell._element.get_or_add_tcPr().append(shading_elm)
    run = p.add_run(text)
    run.font.name = '微软雅黑'
    run._element.rPr.rFonts.set(qn('w:eastAsia'), '微软雅黑')
    run.font.size = Pt(size)
    run.bold = bold
    # White text for dark header
    if bg_color:
        run.font.color.rgb = RGBColor(255, 255, 255)
    return p

def add_table(headers, rows, col_widths=None):
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.style = 'Table Grid'
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, header in enumerate(headers):
        set_cell_text(table.rows[0].cells[i], header, bold=True, size=10,
                      alignment=WD_ALIGN_PARAGRAPH.CENTER, bg_color='2B579A')
    for r, row in enumerate(rows):
        for c, cell_text in enumerate(row):
            set_cell_text(table.rows[r + 1].cells[c], cell_text, size=10)
            if r % 2 == 1:
                shading = parse_xml(f'<w:shd {nsdecls("w")} w:fill="F2F6FC"/>')
                table.rows[r + 1].cells[c]._element.get_or_add_tcPr().append(shading)
    doc.add_paragraph()
    return table

def add_separator():
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    pf = p.paragraph_format
    pf.space_before = Pt(6)
    pf.space_after = Pt(6)
    run = p.add_run('─' * 50)
    run.font.color.rgb = RGBColor(180, 180, 180)
    run.font.size = Pt(8)

